      <p class="section11">Section 4.01</p>
      <p class="section12">Shall and May</p>
    </div>
    <p>Unless otherwise specifically provided in this document or by the context in which used, I use the word shall in this document to impose a duty, command, direction, or requirement, and the word may to allow or permit, but not require.  In the context of my Agent, when I use the word shall, I intend to impose a fiduciary duty on my Agent.  When I use the word may, I intend that my Agent is empowered to act with sole and absolute discretion unless otherwise stated in this document.</p>
