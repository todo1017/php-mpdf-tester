<div> <!-- sub -->
  <p>&nbsp;</p>
  <p>I, _____________, the principal, an adult of sound mind, execute this Healthcare Power of Attorney freely and voluntarily, with an understanding of its purposes and consequences.  I intend to create a medical durable power of attorney under the laws of the State of Arkansas.  I further intend to demonstrate my wishes concerning medical treatment with clear and convincing evidence.  I hereby revoke any Healthcare Power of Attorney previously granted by me as principal except powers granted by me under any state statutory Healthcare Power of Attorney.</p>
  <p>This Healthcare power of Attorney is intended to comply with the Arkansas Rights of Terminally Ill or Permanently Unconscious Act and the Arkansas Healthcare Decisions Act (collectively, the “Acts”) and to the extent that any provision of this Living Will conflicts with a provision of the Acts, the provisions of the Acts will be deemed to control.</p>
</div>
<div> <!-- Article 1 -->
  <div class="center"> <!-- head -->
    <p>&nbsp;</p>
    <h4 class="font16 nomargin">Article One</h4>
    <h4 class="font16 nomargin">Recitals</h4>
    <p>&nbsp;</p>
  </div>
  <div> <!-- Section -->
    <div class="section_head">
      <p class="section11">Section 1.01</p>
      <p class="section12">Designation of Agent</p>
    </div>
    <p>I designate the individuals in the order listed below as Agents to exercise the powers and discretions set forth in this instrument.</p>
    <p class="indent1_strict">{First Choice};</p>
    <p class="indent1_strict">{Second Choice};</p>
    <p class="indent1_strict">{Third Choice}.</p>
    <p class="indent1_strict">{CoAgent1} and {CoAgent2} jointly or the survivor of them as co-Agents.</p>
    <p>I give my Agent the power to make decisions with regard to my health care if I am unable to make my own health care decisions.</p>
  </div>
  <div> <!-- Section -->
    <div class="section_head">
      <p class="section11">Section 1.02</p>
      <p class="section12">Duration</p>
    </div>
    <p>This Healthcare Power of Attorney expires at the earliest of:</p>
    <p class="indent1_strict">my death (except for post-death matters allowed under Arkansas law); or</p>
    <p class="indent1_strict">my revocation of this Healthcare Power of Attorney.</p>
    <p>However, the medical information and medical records provisions described in Section 2.04 continue in effect for an additional 24 months from the date of my death unless revoked.  This power of attorney is intended to be a Durable Power of Attorney for Health Care, in accordance with Act 1264 of the 2013 Arkansas General Assembly known as the Health Care Decisions Act (Ark. Code Ann. § 20-6-101 et seq.) and shall not be affected by my subsequent disability or incapacity.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 1.03</p>
      <p class="section12">Effect of Filing Petition for Divorce or Separation</p>
    </div>
    <p>If my spouse or I file a petition for legal separation or dissolution of marriage, my spouse will be deemed to have died or predeceased me (as appropriate for the context) for all purposes of this Healthcare Power of Attorney unless and until the petition is dismissed.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 1.04</p>
      <p class="section12">General Grant</p>
    </div>
    <p>My Agent may determine and implement all actions necessary for my personal care, residential placement, and medical treatment, including the items specifically mentioned in this instrument.  If my Agent is not available, I intend to guide decisions about my care and treatment with the following statements.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 1.05</p>
      <p class="section12">Effect on Legal Capacity</p>
    </div>
    <p>A formal adjudication of my incapacity is not required for my Agent to exercise the authority granted by me under this instrument.</p>
  </div>
</div>
<div> <!-- Article 2 -->
  <div class="center"> <!-- head -->
    <p>&nbsp;</p>
    <h4 class="font16 nomargin">Article Two</h4>
    <h4 class="font16 nomargin">Health and Personal Powers</h4>
    <p>&nbsp;</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 2.01</p>
      <p class="section12">Instructions Concerning Medical Evaluations and Treatment</p>
    </div>
    <p>In exercising the authority granted to my Agent, I instruct my Agent to discuss with me the specifics of any proposed decision regarding my medical care and treatment if I am able to communicate in any manner however rudimentary, even by blinking my eyes.  I further instruct my Agent that if I am unable to give an informed consent to medical treatment, my Agent shall give or withhold consent based upon any treatment choices I have expressed while competent, whether under this instrument or otherwise.  If my Agent cannot determine the treatment choice I would want made under the circumstances, then I request that my Agent make the choice for me based upon what my Agent believes to be in my best interests.  I request that my Agent’s decision be guided by taking into account:</p>
    <p class="indent1_strict">the provisions of this instrument;</p>
    <p class="indent1_strict">any preferences that I may have expressed on the subject;</p>
    <p class="indent1_strict">what my Agent believes I would want done in the circumstances if I were able to express myself; and</p>
    <p class="indent1_strict">any information given to my Agent by the physicians treating me as to my medical diagnosis and prognosis and the intrusiveness, pain, risks, and side effects of the treatment.</p>
    <p>I want to leave my family, friends, and persons who care about me with assurances of my love, and without the burdens of guilt or conflict.  My purposes in leaving these instructions are to alleviate uncertainty that otherwise may arise in connection with decisions about my medical care, to promote family harmony, and to clarify instructions to my health care providers.  My Agent’s authority to act on my behalf concerning my medical care includes decisions concerning artificial life support, medical treatment, surgery and other medical procedures; artificial nourishment and hydration; resuscitation decisions (including Do Not Resuscitate [DNR] orders and Cardiopulmonary Resuscitation [CPR] directives); amputation of my limbs; blood transfusions; experimental drugs and medical procedures; the administration of pharmaceutical agents; arrangements for my longterm care.</p>
    <p>I affirm my belief in the importance and value of my personal dignity, both in living and in dying.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 2.02</p>
      <p class="section12">Longterm or Hospice Care</p>
    </div>
    <p>My Agent may select a facility for my nursing, convalescent, or hospice care and establish my residence and placement in a secure unit therein if the facility provides the quality of care appropriate for my medical needs and mental condition.  For the purposes of arranging or providing longterm care, my Agent has authority to facilitate my transportation and establish my legal residence within or beyond the state of Arkansas.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 2.03</p>
      <p class="section12">Maintain Me in My Residence</p>
    </div>
    <p>
      I authorize my Agent to take whatever steps are necessary or advisable to enable me to remain in my personal residence as long as it is reasonable under the circumstances.  I realize that my health may deteriorate so that it becomes necessary to have round-the-clock nursing care if I am to remain in my personal residence, and I direct my Agent to obtain that care, including any equipment that might assist in my care, as is reasonable under the circumstances.  Specifically, I do not want to be hospitalized or put in a convalescent or similar home as long as it is reasonable to maintain me in my personal residence.
    </p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 2.04</p>
      <p class="section12">Medical Information and Medical Records</p>
    </div>
    <p>Acting on my behalf, my Agent may have access to all of my medical information and photocopies of my medical records from my health care providers including physicians, dentists, podiatrists, physical therapists, chiropractic physicians and chiropractors, pharmacists, optometrists, psychologists, social workers, hospitals, hospices, and other treatment facilities; may disclose medical and related information concerning my treatment to appropriate health care providers; and may admit or transfer me to such hospitals, hospices, or treatment facilities as my Agent determines to be in my best interests.</p>
    <p>In order for my Agent to fulfill his or her duties, my treating physician or hospital is to discuss my medical condition with and disclose all medical records to my Agent.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 2.05</p>
      <p class="section12">Employ and Discharge Health Care Personnel</p>
    </div>
    <p>My Agent may employ and discharge medical personnel including physicians, psychiatrists, dentists, nurses, and therapists as my Agent determines necessary for my physical, mental, and emotional well-being, and pay them reasonable compensation.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 2.06</p>
      <p class="section12">Pain Relief</p>
    </div>
    <p>I want to ensure that my Agent and physician protect my comfort and freedom from pain insofar as possible.  I authorize my Agent to consent on my behalf to the administration of whatever pain-relieving drugs and pain-relieving surgical procedures my Agent, upon medical advice, believes may provide comfort to me, even though such drugs or procedures may lead to pharmaceutical addictions, lower blood pressure, lower levels of breathing, or hasten my death.  Even if artificial life support or aggressive medical treatment has been withdrawn or refused, I want to be kept as comfortable as possible, and I do not want to be neglected by medical or nursing staff.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 2.07</p>
      <p class="section12">Consent to Psychiatric Treatment</p>
    </div>
    <p>Upon the execution of a certificate by two independent psychiatrists who have examined me and in whose opinions I am in immediate need of hospitalization because of mental disorders, alcoholism, or drug abuse, my Agent may arrange for my voluntary admission to an appropriate hospital or institution for treatment of the diagnosed problem or disorder; to arrange for private psychiatric and psychological treatment for me; and to revoke, modify, withdraw, or change consent to the hospitalization, institutionalization, or private treatment that I or my Agent may have previously given.  The consent of my Agent to my hospitalization for psychiatric help, alcoholism, or drug abuse has the same legal effect, subject to applicable local law, as a voluntary admission made by me.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 2.08</p>
      <p class="section12">Grant Releases</p>
    </div>
    <p>My Agent may grant, in conjunction with any instructions given under this instrument, releases from all liability for damages suffered or to be suffered by me to hospital staff, physicians, nurses, and other medical and hospital administrative personnel who act in reliance on instructions given by my Agent or who render written opinions to my Agent in connection with any matter described in this instrument.  My Agent may sign documents titled or purporting to be a <i>Refusal to Permit Treatment</i> and <i>Leaving Hospital Against Medical Advice</i> as well as any necessary waivers of or releases from liability required by any hospital or physician to implement my wishes regarding medical treatment or nontreatment.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 2.09</p>
      <p class="section12">Living Will</p>
    </div>
    <p>I have executed a Living Will under the laws of the state of Arkansas.  To the extent that any provisions of this Healthcare Power of Attorney conflict with my Living Will, the provisions of my Living Will will prevail.</p>
    <p>If I become unconscious or incompetent in a state where my Living Will or this Healthcare Power of Attorney is not enforceable, I authorize my Agent to transport me or arrange for my transportation to a jurisdiction where my medical directives will be enforceable.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 2.10</p>
      <p class="section12">Autopsy and Disposition of Remains</p>
    </div>
    <p>My Agent may authorize an autopsy and direct the disposition of my remains.</p>
  </div>
</div>
<div> <!-- Article 3 -->
  <div class="center"> <!-- head -->
    <p>&nbsp;</p>
    <h4 class="font16 nomargin">Article Three</h4>
    <h4 class="font16 nomargin">Legal and Administrative Powers and Provisions</h4>
    <p>&nbsp;</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 3.01</p>
      <p class="section12">Health Insurance Portability and Accountability Act</p>
    </div>
    <p>I grant my Agent the power and authority to serve as my authorized recipient for all purposes of the Health Insurance Portability and Accountability Act (HIPAA) of 1996 and its regulations immediately upon my signing this document.</p>
    <p>Pursuant to HIPAA, I specifically authorize my Agent as my HIPAA-authorized recipient to request, receive, and review any information regarding my physical health, including all HIPAA-protected health information, medical, and hospital records; to execute on my behalf any authorizations, releases, or other documents that may be required to obtain this information; and to consent to the disclosure of this information.  I further authorize my Agent to execute on my behalf valid authorizations for the release of HIPAA-protected health information.</p>
    <p>By signing this Healthcare Power of Attorney, I specifically authorize my physician, hospital, or health care provider to release any medical records to my Agent or any person designated in a valid authorization for the release of HIPAA-protected health information executed by my Agent.  Further, I waive any liability to any physician, hospital, or health care provider that releases any of my medical records to my Agent and acknowledge that the health information that would otherwise be protected under HIPAA will no longer be protected.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 3.02</p>
      <p class="section12">Guardian</p>
    </div>
    <p>My Agent’s authority precludes the need for appointment of a Guardian.  But if any proceeding is commenced for the appointment of a Guardian, I nominate my Agent to serve as Guardian.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 3.03</p>
      <p class="section12">Third-Party Reliance</p>
    </div>
    <p>My Agent’s instructions and decisions regarding my medical treatment are binding on third parties.  No person, medical facility, or institution will incur any liability to me or to my estate by complying with my Agent’s instructions.  My Agent is authorized to execute consents, waivers, and releases of liability on my behalf and on behalf of my estate to all medical personnel who comply with my Agent’s instructions.  Furthermore, I authorize my Agent to indemnify and hold harmless, at my expense, any third party who accepts and acts under this Healthcare Power of Attorney, and I agree to be bound by any indemnity entered into by my Agent.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 3.04</p>
      <p class="section12">Enforcement by Agent</p>
    </div>
    <p>I authorize my Agent to seek on my behalf and at my expense:</p>
    <p class="indent1_strict">a declaratory judgment from any court of competent jurisdiction interpreting the validity of this instrument or any of the acts authorized by this instrument, but a declaratory judgment is not required for my Agent to perform any act authorized by this instrument;</p>
    <p class="indent1_strict">an injunction requiring compliance with my Agent’s instructions by any person providing medical or personal care to me; or</p>
    <p class="indent1_strict">actual and punitive damages against any person responsible for providing medical or personal care to me who willfully fails or refuses to follow my Agent’s instructions.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 3.05</p>
      <p class="section12">Release of Agent’s Personal Liability</p>
    </div>
    <p>My Agent will not incur any personal liability to me or my estate arising from the good faith exercise of discretion or performance of acts and duties relating to my medical treatment and personal care.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 3.06</p>
      <p class="section12">Reimbursement of Agent</p>
    </div>
    <p>My Agent is entitled to reimbursement for all reasonable expenses arising from the performance of acts and duties relating to my medical treatment and personal care under this instrument.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 3.07</p>
      <p class="section12">Copies Effective as Originals</p>
    </div>
    <p>Photocopies of this instrument are effective and enforceable as originals, and third parties are entitled to rely on photocopies of this instrument for the full force and effect of all stated terms.  The word photocopies includes facsimiles, digital, or other reproductions.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 3.08</p>
      <p class="section12">Interstate Enforceability</p>
    </div>
    <p>
      My intention is that the terms of this instrument be honored in any jurisdiction, regardless of its conformity to that jurisdiction’s technical requirements and legal formalities.
    </p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 3.09</p>
      <p class="section12">Amendment and Revocation</p>
    </div>
    <p>I reserve the right to revoke my Agent’s authority orally or in writing.</p>
  </div>
  <div> <!-- section -->
    <div class="section_head">
      <p class="section11">Section 3.10</p>
      <p class="section12">Revocation of Prior Powers</p>
    </div>
    <p>Unless specifically excepted in this instrument, this Healthcare Power of Attorney supersedes any prior medical durable power of attorney that I have executed.  But this instrument does not affect any other unrelated powers previously conveyed by me through general or limited powers of attorney, or my Living Will; these powers and Living Will are to continue in full force until revoked by me or otherwise terminated.</p>
  </div>
</div>
