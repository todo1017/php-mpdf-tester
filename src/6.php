  <div class="center bold uppercase">
    <p class="nomargin">ARTICLE IX</p>
    <p>Administrative powers</p>
  </div>
  <p class="indent1">My executor and trustee shall have all the powers conferred by Act 153 of the 1961 Arkansas General Assembly and Act 1031 of the 2005 Arkansas General Assembly, as amended, which may be exercised in a fiduciary capacity upon such terms and conditions as my executor and trustee shall deem advisable and without Court authorization or approval, and said Acts are hereby incorporated in this Will by reference.</p>
</div>
<div> <!-- article 10 -->
  <div class="center bold uppercase">
    <p class="nomargin">ARTICLE X</p>
    <p>construction</p>
  </div>
  <p class="indent1">In carrying out the provisions of this Will, my executor and/or trustee, as the case may be, shall observe the following rules of construction:</p>
  <p class="indent1">(a) Throughout this Will the terms "child" and "children" shall be deemed to include any children born or adopted after the execution of this Will.</p>
  <p class="indent1">(b) Throughout this Will the singular shall be deemed to include the plural and the masculine gender the feminine, and vice versa.</p>
  <p class="indent1">(c) This Will has been executed in, and shall be construed under the laws of, the State of Arkansas, unless otherwise provided herein.</p>
  <p class="indent1">(d) In construing the provisions of each trust or trust fund created in this Will, my trustee shall resolve any doubt as to the payment of income and principal from each trust or trust fund, within the limits recited in the provisions relating thereto, in favor of the beneficiary or beneficiaries thereunder.  Furthermore, it is my wish and direction that the health, education, maintenance and support provisions recited in any trust created herein shall be deemed to include, but shall not be limited to, expenses reasonably incurred by the beneficiary or beneficiaries of each trust or trust fund for medical, dental, hospital care or invalidism, and for public or private college, professional and vocational school education.</p>
</div>
