<div> <!-- article 8 -->
  <div class="center bold uppercase">
    <p class="nomargin">ARTICLE VIII</p>
    <p class="nomargin">Distributions to a Person Under Age Twenty-One,</p>
    <p>Incompetent, or Incapacitated</p>
  </div>
  <p class="indent1">Notwithstanding any prior provision herein to the contrary, whenever pursuant to the provisions of this Will, any portion of my estate, or the income or principal under any trust created hereunder, shall become payable to a person under the age of twenty-one (21), or to a person under legal disability, or to a person not adjudicated incompetent, but who, by reason of illness or mental or physical disability, is, in the opinion of my trustee or executor, as applicable, unable to properly administer such amounts, then such amounts shall be paid out by my trustee or executor, as applicable, in such of the following ways as my trustee or executor, as applicable, deems best: (1) directly to the beneficiary; (2) to the natural or legal guardian of the beneficiary; (3) to some relative or friend for the medical care, education, support and maintenance in reasonable comfort of the beneficiary; (4) by my trustee or executor, as applicable, using such amounts directly for the beneficiary's care, support and education; or (5) to a custodian for the beneficiary under the Arkansas Uniform Transfers to Minors Act.</p>
</div>
