<div> <!-- article 1 -->
  <p class="center bold nomargin">ARTICLE I</p>
  <p class="center bold">PAYMENT OF DEBTS, EXPENSES and TAXES</p>
  <p class="indent1">I direct my executor to pay the expenses of my last illness and burial, and incident to administering my estate, as well as all proper and enforceable claims against my estate, out of assets included in my residuary probate estate and to do so without Court authorization or approval.</p>
  <p class="indent1">My executor shall (but shall not be required) to pay or discharge any mortgage or other indebtedness owed by me which is secured by property given under this Will.</p>
  <p class="indent1">The preceding directions shall not preclude the payment or reimbursement of any such debts, expenses and taxes out of any trust I may have established during my lifetime, to the extent provided under the governing instrument of such trust, as amended to my date of death.</p>
</div>
