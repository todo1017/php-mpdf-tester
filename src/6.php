<div> <!-- article 5 -->
  <p class="center bold nomargin">ARTICLE V</p>
  <p class="center bold uppercase">{CLIENT NAME} FAMILY TRUST</p>
  <p class="indent1">The {Client Name} Family Trust shall be held, administered and distributed as follows:</p>
  <p class="indent1">(a) <i>Distribution of Principal and Income.</i>  The Trustee shall pay over and distribute such portion of the income and/or principal of the trust fund to, or on behalf of, such beneficiary at such time or times as the Trustee may determine, and deem necessary, for such beneficiary’s health, education, maintenance and support in the manner to which he/she was accustomed at my death.  Any income not distributed hereunder shall be added to the principal of the trust fund.</p>
  <p class="indent1">(b) <i>Distribution of Principal and Undistributed Income.</i>  When such beneficiary shall attain the age of thirty (30) years, the Trustee shall pay over and distribute any principal and undistributed income of the trust fund to such beneficiary.  If such beneficiary shall die before a complete distribution of the trust fund, the remaining principal and any undistributed income shall be paid over and distributed to such beneficiary’s surviving descendants, per stirpes, or, if none, to such beneficiary’s heirs, per stirpes, or, if none, to the estate of such beneficiary, unless otherwise inconsistent with prior provisions hereunder.</p>
</div>
