  <p>I give all of my tangible personal property (other than that held for business or investment purposes) to my spouse if my spouse shall survive me.  If my spouse does not survive me, I then give all such property to my surviving children, in equal shares.  My executor shall have the discretion to allocate particular assets or portions thereof or undivided interests therein to any one or more of the preceding beneficiaries as my executor shall deem to be for the best interests of the beneficiaries.  The decision of my executor shall be final and binding upon all parties in interest, subject to the approval of a court or competent jurisdiction.  Notwithstanding the foregoing, I direct my executor to honor, and comply with in full, any memorandum, signed by me and attached to this Will, instructing my executor with respect to the disposition of any specific item or items of my personal effects.</p>
  <p>I give all of my tangible personal property (other than that held for business or investment purposes) to the following surviving individuals and/or entities in such amounts, allocations and percentages as set forth below:</p>
