</div>
<div> <!-- sub -->
  <p>&nbsp;</p>
  <p>I, ___________, of Little Rock, Arkansas, willfully and voluntarily declare that if my death becomes imminent, I am in a permanent vegetative state, or I have a terminal illness or incurable condition, my life shall not be artificially prolonged under the guidelines described below.</p>
  <p>This Living Will is intended to comply with the Arkansas Rights of Terminally Ill or Permanently Unconscious Act and the Arkansas Healthcare Decisions Act (collectively, the “Acts”) and to the extent that any provision of this Living Will conflicts with a provision of the Acts, the provisions of the Acts will be deemed to control.</p>
</div>
<div>
  <p>&nbsp;</p>
  <p class="bold font14 center">Guidelines for the Cessation of Life-Sustaining Treatments</p>
  <p class="indent2">
    (1) <i>Incurable or Irreversible Condition.</i> If I should have an incurable or irreversible condition that will cause my death within a relatively short time with no reasonable expectation of recovery, and I am no longer able to make decisions regarding my medical treatment, I direct my attending physician, pursuant to the Arkansas Rights of the Terminally Ill or Permanently Unconscious Act of 1987, to withhold or withdraw life-sustaining treatments that only prolong the process of dying and are not necessary for my comfort or to alleviate pain.
  </p>
  <p class="indent2">
    (2) <i>Permanently Unconscious.</i>  If I should become permanently unconscious, I direct my attending physician, pursuant to the Arkansas Rights of the Terminally Ill or Permanently Unconscious Act of 1987, to withhold or withdraw life-sustaining treatments that only prolong the process of dying and are not necessary for my comfort or to alleviate pain.
  </p>
  <p class="indent2">
    (3) <i>Life-Sustaining Treatments Withheld.</i> These lifesustaining treatments which may be withheld or withdrawn include, but are not limited to:  ANTIBIOTICS, ARTIFICIALLY ADMINISTERED NUTRITION, ARTIFICIALLY ADMINISTERED HYDRATION, CARDIAC RESUSCITATION, RESPIRATORY SUPPORT, AND SURGERY.  Notwithstanding any state law to the contrary, I hereby claim my constitutional right protected under the Fourteenth Amendment, as expressed by the United States Supreme Court in the case of Cruzan vs. Missouri, to refuse the artificial delivery of food and fluids.
  </p>
  <p>For this declaration to take effect, my attending physician must determine that there can be no recovery from my incurable or irreversible condition and/or permanently unconscious state and that either my death is imminent or I can no longer experience a meaningful life.</p>
  <p>I wish to die naturally, with only the administration of medication or the performance of any medical procedures considered necessary to provide me with comfort and care or to alleviate pain, even though the medication or procedure may shorten my remaining life.</p>
</div>
<div>
  <p>&nbsp;</p>
  <p class="font14 bold center">Statement of My Intent</p>
  <p>In the absence of my ability to give directions regarding the use of such life-sustaining treatments, I intend to express my legal right to refuse medical or surgical treatment and to accept the consequences of such refusal through this declaration, to be honored by my family and physician.</p>
  <p>I make this declaration after careful consideration and in accordance with my strong convictions and beliefs.  I want my wishes and directions expressed in this declaration to be carried out to the extent permitted by law.  Insofar as my wishes and directions are not legally enforceable, I hope that my family, my physician, the courts, and all others who may be involved in such decision-making will regard themselves as morally bound by them.</p>
</div>
<div>
  <p>&nbsp;</p>
  <p class="font14 bold center">Authorization of Surrogate</p>
  <p>If I have named a surrogate for health care decisions or appointed an agent pursuant to a power of attorney to make health care decisions for me, he or she may provide consent for withholding or withdrawing life-sustaining treatments according to my wishes.</p>
</div>
<div>
  <p>&nbsp;</p>
  <p class="font14 bold center">Release of Liability</p>
  <p>I release and hold harmless any person who, in good faith, terminates life-sustaining treatments in accordance with the guidelines in this declaration.</p>
  <p>I understand the full import of this declaration and I am emotionally and mentally competent to make this declaration.</p>
</div>

